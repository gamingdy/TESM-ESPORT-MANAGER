package modele;

public class CompteUtilisateur {

	private String username;
	private String mdp;
	
	public CompteUtilisateur(String username, String mdp) {
		this.username = username;
		this.mdp = mdp;
	}

	public String getUsername() {
		return username;
	}

	public String getMdp() {
		return mdp;
	}


	
	
	
	
}
