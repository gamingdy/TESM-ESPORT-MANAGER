package modele;

public class Selection {
	
	private int idArbitre;
	private int annee;
	
	public Selection(int idArbitre, int i) {
		this.idArbitre = idArbitre;
		this.annee = i;
	}

	public int getIdArbitre() {
		return idArbitre;
	}

	public void setIdArbitre(int idArbitre) {
		this.idArbitre = idArbitre;
	}

	public int getAnnee() {
		return annee;
	}

	public void setAnnee(int annee) {
		this.annee = annee;
	}

	

}
